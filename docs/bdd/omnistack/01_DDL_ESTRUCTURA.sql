/* ============================================================
   ESQUEMA : TUKUNAFUNC
   PROYECTO : OmniStack
   OBJETIVO : Crear tablas de configuracion de proveedores y
              trazabilidad de transacciones.

   PREFIJO  : IN_OMNI_  (evita colision con tablas IN_ del proyecto
              anterior que comparten el mismo esquema TUKUNAFUNC)

   TABLAS CREADAS
   ---------------------------------------------------------------
   IN_OMNI_PROVEEDOR_CONFIG   configuracion funcional por proveedor
   IN_OMNI_PROVEEDOR_WS       catalogo de endpoints por proveedor
   IN_OMNI_PROVEEDOR_HEADERS  headers HTTP por proveedor
   IN_OMNI_PROVEEDOR_WS_DEFS  valores por defecto por WS
   IN_OMNI_LOGS_APP           log de nivel aplicacion OmniStack
   IN_OMNI_LOGS_WS_EXT        log de llamadas a WS externos
   IN_OMNI_REGISTRO_TRX       registro de transacciones completadas

   NOTAS
   ---------------------------------------------------------------
   No se usan triggers. Todos los IDs/CODIGOs se insertan
   explicitamente via SEQ_XXX.NEXTVAL desde el DML o desde Java.
   ============================================================ */

---------------------------------------------------------------
-- 1) TABLA: IN_OMNI_PROVEEDOR_WS
---------------------------------------------------------------
CREATE TABLE TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS (
  ID_WS             NUMBER(12)     NOT NULL,
  PROVEEDOR_KEY     VARCHAR2(50)   NOT NULL,
  WS_KEY            VARCHAR2(120)  NOT NULL,
  ENABLED           CHAR(1)        DEFAULT 'S' NOT NULL,
  TIPO_CONEXION     VARCHAR2(30)   NOT NULL,
  METODO_HTTP       VARCHAR2(10)   NOT NULL,
  TIPO_REQUEST      VARCHAR2(30)   NOT NULL,
  URL               VARCHAR2(500)  NOT NULL,
  NOMBRE_OPERACION  VARCHAR2(200),
  USR_CREACION      VARCHAR2(100)  DEFAULT USER    NOT NULL,
  FEC_CREACION      DATE           DEFAULT SYSDATE NOT NULL,
  USR_MODIFICACION  VARCHAR2(100),
  FEC_MODIFICACION  DATE,

  CONSTRAINT PK_IN_OMNI_PROVEEDOR_WS
    PRIMARY KEY (ID_WS),
  CONSTRAINT CK_IN_OMNI_PROVEEDOR_WS_ENA
    CHECK (ENABLED IN ('S','N')),
  CONSTRAINT CK_IN_OMNI_PROVEEDOR_WS_MET
    CHECK (UPPER(METODO_HTTP) IN ('GET','POST','PUT','PATCH','DELETE'))
);

CREATE UNIQUE INDEX TUKUNAFUNC.UX_IN_OMNI_PROVEEDOR_WS_01
  ON TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS (PROVEEDOR_KEY, WS_KEY);

CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_PROVEEDOR_WS_01
  ON TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS (PROVEEDOR_KEY);

CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_PROVEEDOR_WS_02
  ON TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS (ENABLED, PROVEEDOR_KEY);

CREATE SEQUENCE TUKUNAFUNC.SEQ_IN_OMNI_PROVEEDOR_WS
  START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

COMMENT ON TABLE  TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS IS 'Catalogo de endpoints (WS) por proveedor OmniStack.';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS.PROVEEDOR_KEY    IS 'Clave interna del proveedor: ecuabet, loteria, pega3, tradicional, claro.';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS.WS_KEY           IS 'Clave de la operacion: PRECHECK.CASHIN, EXECUTE.CASHOUT, LOGIN, etc.';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS.URL              IS 'URL completa del WS (base_url concatenado con path).';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS.NOMBRE_OPERACION IS 'Nombre logico de la operacion (ej: DEPOSITO, RECARGA593, validateRechargeRetail).';

