---------------------------------------------------------------
-- 3) TABLA: IN_OMNI_PROVEEDOR_WS_DEFS
---------------------------------------------------------------
CREATE TABLE TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS_DEFS (
  ID_DEFAULT         NUMBER(12)     NOT NULL,
  ID_WS              NUMBER(12)     NOT NULL,
  TIPO_DEF           VARCHAR2(100),
  DEFAULT_CLAVE      VARCHAR2(200)  NOT NULL,
  DEFAULT_VALOR_TEXT VARCHAR2(4000),
  DEFAULT_VALOR_NUM  NUMBER(18,6),
  DEFAULT_VALOR_DATE DATE,
  USR_CREACION       VARCHAR2(100)  DEFAULT USER    NOT NULL,
  FEC_CREACION       DATE           DEFAULT SYSDATE NOT NULL,
  USR_MODIFICACION   VARCHAR2(100),
  FEC_MODIFICACION   DATE,

  CONSTRAINT PK_IN_OMNI_PROVEEDOR_WS_DEFS
    PRIMARY KEY (ID_DEFAULT),
  CONSTRAINT FK_IN_OMNI_WS_DEFS_WS
    FOREIGN KEY (ID_WS)
    REFERENCES TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS (ID_WS)
);

CREATE UNIQUE INDEX TUKUNAFUNC.UX_IN_OMNI_PROVEEDOR_WS_DEFS_01
  ON TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS_DEFS (ID_WS, DEFAULT_CLAVE);

CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_PROVEEDOR_WS_DEFS_01
  ON TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS_DEFS (ID_WS);

CREATE SEQUENCE TUKUNAFUNC.SEQ_IN_OMNI_PROVEEDOR_WS_DEFS
  START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

COMMENT ON TABLE  TUKUNAFUNC.IN_OMNI_PROVEEDOR_WS_DEFS IS 'Valores por defecto configurables por WS de proveedor.';

