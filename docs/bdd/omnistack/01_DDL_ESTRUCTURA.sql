  MONEDA                    VARCHAR2(10)   DEFAULT 'USD',
  COD_ESTADO                VARCHAR2(20),
  ES_ERROR                  CHAR(1),
  COD_ERROR                 VARCHAR2(200),
  MSG_ERROR                 VARCHAR2(2000),
  CP_VAR1                   VARCHAR2(1500),
  CP_VAR2                   VARCHAR2(1500),
  CP_VAR3                   VARCHAR2(1500),
  CP_NUMBER1                NUMBER,
  CP_DATE1                  DATE,

  CONSTRAINT PK_IN_OMNI_REGISTRO_TRX
    PRIMARY KEY (CODIGO),
  CONSTRAINT CK_IN_OMNI_REGISTRO_TRX_ERR
    CHECK (ES_ERROR IN ('S','N')),
  CONSTRAINT CK_IN_OMNI_REGISTRO_TRX_MON
    CHECK (MONEDA IS NULL OR LENGTH(MONEDA) <= 10)
