-- 5) TABLA: IN_OMNI_LOGS_APP
--    CODIGO se inserta via SEQ_IN_OMNI_LOGS_APP.NEXTVAL desde Java.
---------------------------------------------------------------
CREATE TABLE TUKUNAFUNC.IN_OMNI_LOGS_APP (
  CODIGO             NUMBER(18)     NOT NULL,
  UUID               VARCHAR2(200),
  REQUEST            CLOB,
  RESPONSE           CLOB,
  USUARIO            VARCHAR2(100),
  FECHA_REGISTRO     DATE           DEFAULT SYSDATE,
  MENSAJE            VARCHAR2(2000),
  PROVEEDOR          VARCHAR2(100),
  CAPABILITY         VARCHAR2(50),
  CANAL              VARCHAR2(50),
  CADENA             NUMBER(10),
  FARMACIA           NUMBER(10),
  NOMBRE_FARMACIA    VARCHAR2(200),
  POS                VARCHAR2(20),
  URL                VARCHAR2(500),
  METODO             VARCHAR2(20),
  HTTP_STATUS        NUMBER(5),
  ES_ERROR           CHAR(1),
  COD_ERROR          VARCHAR2(200),
  MSG_ERROR          VARCHAR2(2000),
  CP_VAR1            VARCHAR2(1500),
  CP_VAR2            VARCHAR2(1500),
  CP_VAR3            VARCHAR2(1500),
  CP_NUMBER1         NUMBER,
  CP_NUMBER2         NUMBER,
  CP_DATE1           DATE,
  CP_DATE2           DATE,

  CONSTRAINT PK_IN_OMNI_LOGS_APP
    PRIMARY KEY (CODIGO),
  CONSTRAINT CK_IN_OMNI_LOGS_APP_ERR
    CHECK (ES_ERROR IN ('S','N'))
);

CREATE SEQUENCE TUKUNAFUNC.SEQ_IN_OMNI_LOGS_APP
  START WITH 1 INCREMENT BY 1 CACHE 500 NOCYCLE;

CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_LOGS_APP_UUID       ON TUKUNAFUNC.IN_OMNI_LOGS_APP (UUID);
CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_LOGS_APP_FECHA      ON TUKUNAFUNC.IN_OMNI_LOGS_APP (FECHA_REGISTRO);
CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_LOGS_APP_PROV_FECHA ON TUKUNAFUNC.IN_OMNI_LOGS_APP (PROVEEDOR, FECHA_REGISTRO);
CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_LOGS_APP_CAP_FECHA  ON TUKUNAFUNC.IN_OMNI_LOGS_APP (CAPABILITY, FECHA_REGISTRO);
CREATE INDEX TUKUNAFUNC.IX_IN_OMNI_LOGS_APP_CAD_FAR    ON TUKUNAFUNC.IN_OMNI_LOGS_APP (CADENA, FARMACIA, POS, FECHA_REGISTRO);

COMMENT ON TABLE  TUKUNAFUNC.IN_OMNI_LOGS_APP IS 'Auditoria de llamadas al API OmniStack (nivel aplicacion).';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_LOGS_APP.UUID       IS 'Identificador de transaccion propagado a todos los proveedores.';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_LOGS_APP.CAPABILITY IS 'Capacidad ejecutada: PRECHECK, EXECUTE, VERIFY, REVERSE, CREATE_TICKET.';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_LOGS_APP.ES_ERROR   IS 'S=error en la transaccion, N=exito.';

