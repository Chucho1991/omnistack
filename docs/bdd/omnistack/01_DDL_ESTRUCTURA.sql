  CODIGO                    NUMBER(18)     NOT NULL,
  UUID                      VARCHAR2(200),
  CADENA                    NUMBER(10),
  FARMACIA                  NUMBER(10),
  NOMBRE_FARMACIA           VARCHAR2(200),
  POS                       VARCHAR2(20),
