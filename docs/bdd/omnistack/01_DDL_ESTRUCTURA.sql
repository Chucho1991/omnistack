COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_REGISTRO_TRX.AUTHORIZATION IS 'Campo authorization del response OmniStack.';
COMMENT ON COLUMN TUKUNAFUNC.IN_OMNI_REGISTRO_TRX.COD_ESTADO    IS 'Estado: OK | ERROR | PENDIENTE | REVERTIDO.';
