--   VERIFY         → authorization del EXECUTE (POS lo reenvía = ventaId)
--   REVERSE        → motivo (cajero escribe) + authorization del EXECUTE (POS lo reenvía)
--
-- CLARO CASH_IN:
--   PRECHECK       → phone (celular a recargar) + amount
--                    POS persiste: authorization (=AUTHORIZATIONNUMBER)
