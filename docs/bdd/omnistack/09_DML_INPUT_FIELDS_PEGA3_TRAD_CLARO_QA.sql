-- FLUJO DE CAMPOS POR PROVEEDOR
-- ─────────────────────────────────────────────────────────────
-- PEGA3 CASH_IN:
--   PRECHECK       → sin user input (devuelve game_data + active_draw)
--   CREATE_TICKET  → ticket_data (objeto complejo construido por POS, no simple input)
