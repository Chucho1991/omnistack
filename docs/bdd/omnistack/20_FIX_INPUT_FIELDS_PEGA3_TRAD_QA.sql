-- 20_FIX_INPUT_FIELDS_PEGA3_TRAD_QA.sql
-- Corrige CATEGORY_CODE, SUBCATEGORY_CODE y SERVICE_PROVIDER_CODE
-- en IN_OMNI_INPUT_FIELDS para items de Pega3 y Tradicionales.
--
-- Problema: script 09 insertó valores dev (cat='1', subcat='2', svc='407827').
-- El adapter construye ServiceKey(cat, subcat, svc, rms_item_code) para el
-- lookup de input_fields. Si no coincide con la ServiceKey del servicio
-- (cat='984', subcat=SUBCLASS_QA, svc='3445'), el resultado es lista vacía.
--
-- Valores QA (fuente: AD_SERVICIO_PARAMETROS + ITEM_MASTER, ver script 19):
--   TERCERO  = 3445   (SERVICE_PROVIDER_CODE)
--   CLASS    = 984    (CATEGORY_CODE)
--   SUBCLASS por item:
--     100708852 (Pega3)    = 1127
--     100713848 (Pega4)    = 1125
--     100713850 (Pega2)    = 1126
--     100713842 (Lotería)  = 1122
--     100713844 (Lotto)    = 1123
--     100713846 (Pozzo)    = 1124
-- ============================================================

-- Pega3 (100708852) — SUBCLASS 1127
UPDATE TUKUNAFUNC.IN_OMNI_INPUT_FIELDS
    SET CATEGORY_CODE = '984', SUBCATEGORY_CODE = '1127', SERVICE_PROVIDER_CODE = '3445'
    WHERE RMS_ITEM_CODE = '100708852';

-- Pega4 (100713848) — SUBCLASS 1125
UPDATE TUKUNAFUNC.IN_OMNI_INPUT_FIELDS
    SET CATEGORY_CODE = '984', SUBCATEGORY_CODE = '1125', SERVICE_PROVIDER_CODE = '3445'
    WHERE RMS_ITEM_CODE = '100713848';

-- Pega2 (100713850) — SUBCLASS 1126
UPDATE TUKUNAFUNC.IN_OMNI_INPUT_FIELDS
    SET CATEGORY_CODE = '984', SUBCATEGORY_CODE = '1126', SERVICE_PROVIDER_CODE = '3445'
    WHERE RMS_ITEM_CODE = '100713850';

-- Lotería Nacional (100713842) — SUBCLASS 1122
UPDATE TUKUNAFUNC.IN_OMNI_INPUT_FIELDS
    SET CATEGORY_CODE = '984', SUBCATEGORY_CODE = '1122', SERVICE_PROVIDER_CODE = '3445'
    WHERE RMS_ITEM_CODE = '100713842';

-- Lotto (100713844) — SUBCLASS 1123
UPDATE TUKUNAFUNC.IN_OMNI_INPUT_FIELDS
    SET CATEGORY_CODE = '984', SUBCATEGORY_CODE = '1123', SERVICE_PROVIDER_CODE = '3445'
    WHERE RMS_ITEM_CODE = '100713844';

-- Pozzo Millonario (100713846) — SUBCLASS 1124
UPDATE TUKUNAFUNC.IN_OMNI_INPUT_FIELDS
    SET CATEGORY_CODE = '984', SUBCATEGORY_CODE = '1124', SERVICE_PROVIDER_CODE = '3445'
    WHERE RMS_ITEM_CODE = '100713846';

